// Bake the LiDAR relief (hillshaded DTM) base layer for the qanat window into assets/.
// Fetches the ACT LiDAR 2019 bare-earth DTM rendering (colour-hypsometric hillshade) from
// the geoportail.lu INSPIRE WMS as a single georeferenced JPEG. At 2048 px over ~1.7 km the
// ground resolution is ~0.85 m/px — near the native 0.5 m — fine enough to show the
// micro-relief (paths, lynchets, hollows) used to hunt shaft-funnel depressions.
//
// Committed to the repo (the static build never refetches). Network required.
// Usage: node scripts/bake-hillshade.mjs
import { writeFileSync } from 'node:fs';

// Must match index.html GEO_BBOX so the drape's per-vertex UV = lon/lat fraction.
const GEO_BBOX = { west: 6.138, east: 6.162, south: 49.6618, north: 49.6708 };
const LAYER = 'EL_ElevationGridCoverage_DTM_2019';   // same epoch as the baked terrain grid
const WIDTH = 4096;   // ~0.42 m/px over the window — near the native 0.5 m DTM resolution

// Keep pixels ~square in ground metres (lon-metres : lat-metres at this latitude).
const midLat = (GEO_BBOX.south + GEO_BBOX.north) / 2;
const mPerDegLon = 111320 * Math.cos(midLat * Math.PI / 180);
const lonM = (GEO_BBOX.east - GEO_BBOX.west) * mPerDegLon;
const latM = (GEO_BBOX.north - GEO_BBOX.south) * 111320;
const HEIGHT = Math.round(WIDTH * latM / lonM);

// WMS 1.3.0 with EPSG:4326 uses lat,lon axis order → bbox = south,west,north,east.
const url = 'https://wms.inspire.geoportail.lu/geoserver/el/ows'
  + '?service=WMS&version=1.3.0&request=GetMap'
  + '&layers=' + LAYER + '&styles='
  + '&crs=EPSG:4326'
  + '&bbox=' + [GEO_BBOX.south, GEO_BBOX.west, GEO_BBOX.north, GEO_BBOX.east].join(',')
  + '&width=' + WIDTH + '&height=' + HEIGHT
  + '&format=image/jpeg';

const OUT_IMG = new URL('../assets/hillshade-walferdange.jpg', import.meta.url);
const OUT_META = new URL('../data/hillshade.json', import.meta.url);

console.log('› fetching LiDAR DTM relief', LAYER, WIDTH + '×' + HEIGHT, '…');
const res = await fetch(url);
const ct = res.headers.get('content-type') || '';
if (!res.ok || !ct.startsWith('image/')) {
  const body = await res.text().catch(() => '');
  console.error('✗ hillshade fetch failed: http', res.status, ct, '\n', body.slice(0, 400));
  process.exit(1);
}
const buf = Buffer.from(await res.arrayBuffer());
writeFileSync(OUT_IMG, buf);
console.log('› wrote assets/hillshade-walferdange.jpg (' + buf.length + ' bytes)');

const meta = {
  _doc: 'GENERATED by scripts/bake-hillshade.mjs — provenance for the LiDAR-relief drape '
    + 'texture (assets/hillshade-walferdange.jpg). bbox matches index.html GEO_BBOX so '
    + 'per-vertex UV = lon/lat fraction. Colour-hypsometric hillshade rendering of the '
    + 'bare-earth DTM: modern micro-relief for archaeological prospection context.',
  hillshade: {
    id: 'raschpetzer-hillshade',
    provider: 'ACT (Administration du cadastre et de la topographie), Luxembourg',
    service: 'geoportail.lu INSPIRE WMS',
    layer: LAYER,
    year: 2019,
    bbox: GEO_BBOX,
    epsg: 4326,
    imagePx: [WIDTH, HEIGHT],
    approxGroundResM: +(lonM / WIDTH).toFixed(2),
    file: 'assets/hillshade-walferdange.jpg',
    source: 'act-lidar-2019',
    status: 'documented',
    note: 'Hillshaded 0.5 m bare-earth DTM (rendered by the WMS at ~0.85 m/px here). Shows '
      + 'the micro-relief used to hunt funnel depressions of the unlocated shafts '
      + '(P-2 / P-3 / P-6A) — see BACKLOG geodesy items.'
  }
};
writeFileSync(OUT_META, JSON.stringify(meta, null, 2) + '\n');
console.log('› wrote data/hillshade.json');
